package com.bt.nextgen.service.hpx;

public class HtmlTranslationException extends Exception {
	public HtmlTranslationException() {
		super();
	}

	public HtmlTranslationException(String message) {
		super(message);
	}

	public HtmlTranslationException(Throwable cause) {
		super(cause);
	}

	public HtmlTranslationException(String message, Throwable cause) {
		super(message, cause);
	}
}
