package com.bt.nextgen.blockcodes.model;


public class Blockcodes 
{
	private String blockcodeId;
	private String blockcodeDesc;
	private String clientId;
	
	
	public String getBlockcodeDesc() {
		return blockcodeDesc;
	}
	public void setBlockcodeDesc(String blockcodeDesc) {
		this.blockcodeDesc = blockcodeDesc;
	}
	public String getClientId() {
		return clientId;
	}
	public void setClientId(String clientId) {
		this.clientId = clientId;
	}
	public String getBlockcodeId() {
		return blockcodeId;
	}
	public void setBlockcodeId(String blockcodeId) {
		this.blockcodeId = blockcodeId;
	}
}
